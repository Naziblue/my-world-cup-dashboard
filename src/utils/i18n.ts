// Translate helper for UI text
export function t(text: string, lang: 'en' | 'fa'): string {
  if (lang === 'en') return text;
  return uiTranslations[text] || text;
}

// Translate helper for Team names
export function translateTeam(name: string, lang: 'en' | 'fa'): string {
  if (lang === 'en') return name;
  const normalized = name.toLowerCase().trim();
  
  // Try exact lookup
  if (teamTranslations[normalized]) return teamTranslations[normalized];
  
  // Try partial mappings (e.g. "Congo DR" vs "DR Congo")
  for (const key of Object.keys(teamTranslations)) {
    if (normalized.includes(key) || key.includes(normalized)) {
      return teamTranslations[key];
    }
  }
  return name;
}

// Format numbers to Persian digits if Farsi is selected
const faDigits = ['۰', '۱', '۲', '۳', '۴', '۵', '۶', '۷', '۸', '۹'];
export function formatNumber(num: number | string | null | undefined, lang: 'en' | 'fa'): string {
  if (num === null || num === undefined) return '';
  const str = String(num);
  if (lang === 'en') return str;
  return str.replace(/[0-9]/g, (w) => faDigits[parseInt(w)]);
}
